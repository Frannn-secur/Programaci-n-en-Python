print("Dame billetes_200 números y operamos con ellos: ")

print("Numero 1: ")
numero_1 = float(input())
print("Numero 2: ")
numero_2 = float(input())

suma = numero_2 + numero_1
print("Suma de ambos números: " + str(suma))

resta = numero_2 - numero_1
print("La resta ambos números: " + str(resta))

division = numero_2 / numero_1
print("La división de ambos números: " + str(division))

multiplicacion = numero_2 * numero_1
print("La multiplicación de ambos números: " + str(multiplicacion))




